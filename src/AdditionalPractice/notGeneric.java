package AdditionalPractice;

public class notGeneric {
	private Object key;
	private Object value;
	
	public notGeneric(Object key, Object value)
	{
		this.key = key;
		this.value = value;
	}
	
	public Object getKey()
	{
		return this.key;
	}
	
	public Object getValue()
	{
		return this.value;
	}

}
